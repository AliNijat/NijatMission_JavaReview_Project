package Day4_Variables_Practice;

public class Deffierence_Between_CharAndInt {

    public static void main(String[] args) {

        char c7 = '1';

        System.out.println( c7); // char: '1'
        System.out.println(c7 + 1); // '1' + 1 = 50

        int r7 = c7;
        System.out.println(r7); // int: 49

        char charz = 'z';
        System.out.println(charz);
        int charz1 = 'z';
        System.out.println(charz1);

        System.out.println("============== ");

        char a1 = 'A';
        System.out.println(a1);

        int a2 = '@' + 65;
        char a3 = '@' + 65;
        System.out.println(a2);
        System.out.println(a3);

        System.out.println( a2 + a3);



    }
}
